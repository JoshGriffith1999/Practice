#Binary AND
a = 50
b = 25
c = a & b
print("A:", a,"B:", b,"AND C:", c)

#Binary OR
a = 50
b = 25
c = a | b
print("A:", a,"B:", b,"OR C:", c)

#BINARY XOR
a = 50
b = 25
c = a ^ b
print("A:", a,"B:", b,"XOR C:", c)

#Right shift
x = 240
y = x >> 2
print("Before:", x, "Right Shifted:", y)

#Left Shifht
x = 240
y = x << 2
print("Before:", x, "Left Shifted:", y)

#BINARY Completement
x = 50
y = ~x
print("Original:", x, "Completement:", y)